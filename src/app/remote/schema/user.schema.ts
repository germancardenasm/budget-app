import { Schema } from 'mongoose';

export const userSchema = new Schema(
  {
    name: {
      required: true,
      type: String,
    },
    lastName: {
      required: true,
      type: String,
    },
    email: {
      required: true,
      type: String,
      unique: true,
    },
    password: {
      required: true,
      type: String,
    },
  },
  { collection: 'users', timestamps: true }
);
